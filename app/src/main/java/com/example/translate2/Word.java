package com.example.translate2;

public class Word {
    String from_lang;
    String to_lang;
    String from;
    String to;

    public Word(String from_lang, String to_lang, String from, String to) {
        this.from_lang = from_lang;
        this.to_lang = to_lang;
        this.from = from;
        this.to = to;
    }
}
